char	*ft_strcpy(char *dst, char const *src)
{
	char	*temp;
	int		i;

	i = 0;
	temp = dst;
	while (src[i])
	{
		temp[i] = src[i];
		i++;
	}
	temp[i] = '\0';
	return (dst);
}
